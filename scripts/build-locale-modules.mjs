/**
 * Reads repo-root `strings.constants.js` (`export const i18n = { ... }`),
 * renames `jp` → `ja`, maps legacy Stripe agent key to the two current keys,
 * writes partial `labels` (runtime merges with `src/constants/defaultLabels.js`),
 * and regenerates `src/utils/localeImports.js`.
 *
 * Place your constants file at: <repo>/strings.constants.js
 * Run: node scripts/build-locale-modules.mjs
 * Or:  npm run build:locales
 */
import fs from "fs";
import path from "path";
import vm from "vm";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const constantsPath = path.join(root, "strings.constants.js");
const outDir = path.join(root, "src/locales");
const localeImportsPath = path.join(root, "src/utils/localeImports.js");

function parseI18nFromFile(filePath) {
  const raw = fs.readFileSync(filePath, "utf8");
  if (!raw.includes("i18n")) {
    throw new Error("strings.constants.js: expected i18n export");
  }
  const patched = raw.replace(
    /export\s+const\s+i18n\s*=/,
    "globalThis.__DOCSBOT_I18N ="
  );
  const ctx = { globalThis: {}, console };
  vm.createContext(ctx);
  vm.runInContext(patched, ctx);
  const i18n = ctx.globalThis.__DOCSBOT_I18N;
  if (!i18n || typeof i18n !== "object") {
    throw new Error("strings.constants.js: could not parse i18n object");
  }
  return i18n;
}

function remapLabels(labels) {
  const out = { ...labels };
  const legacy = out.agentActivityStripeRecentInvoicesAndSubscriptions;
  if (legacy != null) {
    if (out.agentActivityStripeRecentInvoices == null) {
      out.agentActivityStripeRecentInvoices = legacy;
    }
    if (out.agentActivityStripeCustomerSubscriptions == null) {
      out.agentActivityStripeCustomerSubscriptions = legacy;
    }
    delete out.agentActivityStripeRecentInvoicesAndSubscriptions;
  }
  return out;
}

function stringifyLocale(code, entry) {
  const labels = remapLabels(entry.labels || {});
  const meta = {
    name: entry.name || code,
    isRTL: Boolean(entry.isRTL),
    labels,
  };
  return `// Auto-generated by scripts/build-locale-modules.mjs — do not edit by hand
export default ${JSON.stringify(meta, null, 2)};
`;
}

function writeLocaleImports(localeCodes) {
  const sorted = [...localeCodes].filter((c) => c !== "en").sort();
  const lines = sorted.map(
    (code) =>
      `  ${code}: () => import(/* webpackChunkName: "locale-${code}" */ "../locales/${code}.js"),`
  );
  const body = `/**
 * Auto-generated by scripts/build-locale-modules.mjs — do not edit by hand.
 * Static import map so webpack emits one async chunk per locale.
 */
export const LOCALE_IMPORTERS = {
${lines.join("\n")}
};
`;
  fs.writeFileSync(localeImportsPath, body, "utf8");
}

async function main() {
  fs.mkdirSync(outDir, { recursive: true });

  if (!fs.existsSync(constantsPath)) {
    console.error(
      `Missing ${constantsPath}\n` +
        "Copy your strings.constants.js to the repository root, then run:\n" +
        "  npm run build:locales"
    );
    process.exit(1);
  }

  const i18n = parseI18nFromFile(constantsPath);

  if (i18n.jp && !i18n.ja) {
    i18n.ja = i18n.jp;
    delete i18n.jp;
  }

  const written = [];
  for (const [code, entry] of Object.entries(i18n)) {
    if (!/^[a-z]{2}$/.test(code)) continue;
    if (code === "en") {
      console.warn(
        "Skipping i18n.en — English lives in src/constants/defaultLabels.js"
      );
      continue;
    }
    const filePath = path.join(outDir, `${code}.js`);
    fs.writeFileSync(filePath, stringifyLocale(code, entry), "utf8");
    written.push(code);
  }

  writeLocaleImports(written);
  console.log(`Wrote ${written.length} locales to ${outDir}`);
  console.log(`Updated ${localeImportsPath}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
